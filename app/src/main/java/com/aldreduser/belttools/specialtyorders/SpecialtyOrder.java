package com.aldreduser.belttools.specialtyorders;

class SpecialtyOrder {
    String orderNum;
    String info;
    String note;

    SpecialtyOrder() {
    }

    String getOrderNum() {
        return orderNum;
    }
    String setOrderNum() {
        this.orderNum = orderNum;
    }

    String getInfo() {
        return info;
    }
    String setInfo() {
        this.info = info;
    }

    String getNote() {
        return note;
    }
    String setNote() {
        this.note = orderNum;
    }
}
